// 2. Callback —Å –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–º —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º: —Ä–∞–∑–æ–≥—Ä–µ–≤ –∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–µ –ø–æ–∂–µ–ª–∞–Ω–∏—è
