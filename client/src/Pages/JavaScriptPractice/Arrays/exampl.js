// Задача 1: Создание и наполнение массива
/*
Создайте массив из 10 случайных чисел от 1 до 100.
Выведите массив в консоль.
*/

// Решение:
const numbers = Array.from({length: 10}, () => Math.floor(Math.random() * 100) + 1);
console.log(`Задача 1: Создание и наполнение массива: ${numbers}`);

// Задача 2: Фильтрация массива
/*
Отфильтруйте массив чисел, оставив только четные числа.
Создайте новый массив с результатом.
*/

// Решение:
const evenNumbers = numbers.filter(num => num % 2 === 0);
console.log(`Задача 2: Фильтрация массива: ${evenNumbers}`);

// Задача 3: Преобразование массива
/*
Создайте массив строк с именами.
Преобразуйте все имена к верхнему регистру.
*/

// Решение:
const names = ['anna', 'ivan', 'peter', 'maria'];
const upperNames = names.map(name => name.toUpperCase());
console.log(`Задача 3: Преобразование массива: ${upperNames}`);

// Задача 4: Поиск в массиве
/*
Найдите первый элемент массива, который больше 50.
Используйте метод find().
*/

// Решение:
const firstOver50 = numbers.find(num => num > 50);
console.log(`Задача 4: Поиск в массиве: ${numbers}`);

// Задача 5: Сортировка массива
/*
Отсортируйте массив чисел по убыванию.
*/

// Решение:
const sortedDesc = [...numbers].sort((a, b) => b - a);
