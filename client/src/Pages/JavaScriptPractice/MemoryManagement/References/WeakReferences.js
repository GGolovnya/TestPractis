// Пример слабых ссылок
let obj = { data: "some data" };
let weakMap = new WeakMap();
weakMap.set(obj, "дополнительные данные");

// WeakSet пример
let weakSet = new WeakSet();
weakSet.add(obj);

// Когда мы обнулим последнюю сильную ссылку
obj = null;

// Теперь объект может быть собран сборщиком мусора
// weakMap и weakSet автоматически очистятся

// Примечание: мы не можем проверить содержимое WeakMap/WeakSet
// после удаления объекта, так как мы не знаем, когда именно
// сработает сборщик мусора