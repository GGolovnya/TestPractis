// Цикл for...of для массивов
const array = ['apple', 'banana', 'orange'];
for (const item of array) {
    console.log(item);
}

// For...of со строками
const str = 'Hello';
for (const char of str) {
    console.log(char);
}

// For...of с Set
const set = new Set([1, 2, 3]);
for (const value of set) {
    console.log(value);
}